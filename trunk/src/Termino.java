
public class Termino {
        
	    private int valor;
        private int exp;
        
        public Termino(){
                super();
        }
        
        public void setValor(int valor) {
                this.valor = valor;
        }
        
        public int getValor() {
                return valor;
        }
        
        public void setExp(int exp) {
                this.exp = exp;
        }
        
        public int getExp() {
                return exp;
        }
}

